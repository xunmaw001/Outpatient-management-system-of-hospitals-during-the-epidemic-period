      }
                                                            if((!this.ruleForm.mima)&& 'yonghu'==this.flag){
        this.$message.error('密码不能为空');
        return
      }
                                                                                                                                                                                                                                          if( 'yonghu' ==this.flag && this.ruleForm.shenfenzheng&&(!checkIdCard(this.ruleForm.shenfenzheng))){
        this.$message.error(`身份证应输入身份证格式`);
        return
      }
                                                if( 'yonghu' ==this.flag && this.ruleForm.shouji&&(!isMobile(this.ruleForm.shouji))){
        this.$message.error(`手机应输入电话格式`);
        return
      }
                                                                                                                  if((!this.ruleForm.gonghao)&& 'yisheng'==this.flag){
        this.$message.error('工号不能为空');
        return
      }
                                                            if((!this.ruleForm.mima)&& 'yisheng'==this.flag){
        this.$message.error('密码不能为空');
        return
      }
                                                                                                                                                                                                                                                                  if( 'yisheng' ==this.flag && this.ruleForm.feiyong&&(!isIntNumer(this.ruleForm.feiyong))){
       this.$message.error(`费用应输入整数`);
        return
      }
